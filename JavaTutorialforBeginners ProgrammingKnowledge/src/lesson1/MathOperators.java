package lesson1;

public class MathOperators {

	public static void main(String[] args) {
		// variables: +, -, *, /, % (modulus)
		
		int x, y, answer;
		
		x = 20;
		y = 30;
		answer = x + y;
		System.out.println("Answer = " + answer);
		
		answer = x - y;
		System.out.println("Answer = " + answer);
		
		answer = x * y;
		System.out.println("Answer = " + answer);
		
		double x1, y1, answer1;
		x1 = 70;
		y1 = 30;
		answer1 = x1 / y1;
		System.out.println("Answer = " + answer1);
		
		x = 70;
		y = 30;
		answer = x % y;
		System.out.println("Answer = " + answer);
		
	}

}
