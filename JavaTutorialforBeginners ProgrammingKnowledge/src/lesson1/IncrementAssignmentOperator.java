package lesson1;

public class IncrementAssignmentOperator {

	public static void main(String[] args) {
		// x++ - print and then increment after ++ operation, ++x - increment after ++ operation and then print
		int x = 10;
		
		/*
		 * x += 5; - adds 5
		 * x -= 5; - subtracts 5
		 * x *= 5; - multiplies by 5
		 * x /= 5; - divides by 5
		 */
		x += 5;
		System.out.println(x);
	}

}
