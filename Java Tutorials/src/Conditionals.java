// Conditionals.java     Java Tutorials
//
// Makes use of the conditional operators
// and prints out the result.
//***************************************
public class Conditionals {

	public static void main(String[] args) {
		int a = 1;
		int b = 2;
		boolean result;
		
		if (a < b) {
			result = true;
			System.out.println(result);
		} else {
			result = false;
			System.out.println(result);
		}
	}
}
